namespace ArgonVisual;

/// <summary>
/// File extensions for various Argon file types.
/// </summary>
public static class ArgonFileExtensions
{
    /// <summary>
    /// The file extension for <see cref="ArgonProject"/>.
    /// </summary>
    public const string Project = ".argproj";

    /// <summary>
    /// The file extension for <see cref="ArgonSolution"/>.
    /// </summary>
    public const string Soluion = ".argsln";

    /// <summary>
    /// The file extension for <see cref="ArgonCodeFile"/>.
    /// </summary>
    public const string CodeFile = ".argon";
}
